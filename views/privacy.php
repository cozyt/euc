<h2>Cookies – How And Why Do We Use Them?</h2>

<p>A cookie is a small text file that is sent to your computer through your web browser when you visit certain websites. This information is stored, in a non-personally identifiable way, for the next time you visit the site.</p>

<p>When you visit our website we send you a cookie. Cookies may be used in the following ways:</p>

<ul>
    <li>To help us attune the content or advertisements to your interests or to refrain from showing you the same adverts repeatedly</li>
    <li>To help us understand how visitors use the website and helps us continue to develop and enhance our user experience. All information collected is anonymous.</li>
</ul>

<h2>Which Cookies Are Being Used On This Site?</h2>

<h3>Cookie Acceptance - cookies_accepted</h3>

<p>Records if you have accepted the use of cookies on the website. It does not contain any user information. This cookie remains on your computer after the session has closed. Records if you have been notified of new cookie information on the site. This cookie prevents the yellow notification banner from appearing on future visits to the site. It does not contain any user information. This cookie remains on your computer after the session has closed.</p>

<h3>Google Analytics - _ga, _gat</h3>

<p>These cookies are used to collect information about how visitors use our site. They are placed on the site by Google, Inc. an American Corporation. It collects information in an anonymous form, including the number of visitors to the site, where visitors have come to the site from and the pages they visited on the site. This information is then used by Google to make reports for us and to help us improve the site. For more information visit http://www.google.com/intl/en/policies/privacy/</p>

<h3>Session - laravel_session</h3>

<p>A user's session cookie (also known as an in-memory cookie or transient cookie) for a website exists in temporary memory only while the user is reading and navigating the website. When an expiry date or validity interval is not set at cookie creation time, a session cookie is created. Web browsers normally delete session cookies when the user closes the browser.</p>

<h3>Reject Or Accept Optional Cookies From The Site:</h3>

<p>You are given the choice to refuse cookies, if you wish, simply by updating the settings in your browser. Please see the ‘Help’ section in your browser on how to do this. This may limit your experience on the site, however, making you unable to view certain interactive features.</p>

